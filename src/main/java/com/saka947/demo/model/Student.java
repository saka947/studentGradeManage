package com.saka947.demo.model;

public class Student {
    private String stuId;

    private String stuName;

    private Integer stuAge;

    private String stuDepartId;

    private String stuAddress;

    private String stuClassId;

    public String getStuId() {
        return stuId;
    }

    public void setStuId(String stuId) {
        this.stuId = stuId;
    }

    public String getStuName() {
        return stuName;
    }

    public void setStuName(String stuName) {
        this.stuName = stuName;
    }

    public Integer getStuAge() {
        return stuAge;
    }

    public void setStuAge(Integer stuAge) {
        this.stuAge = stuAge;
    }

    public String getStuDepartId() {
        return stuDepartId;
    }

    public void setStuDepartId(String stuDepartId) {
        this.stuDepartId = stuDepartId;
    }

    public String getStuAddress() {
        return stuAddress;
    }

    public void setStuAddress(String stuAddress) {
        this.stuAddress = stuAddress;
    }

    public String getStuClassId() {
        return stuClassId;
    }

    public void setStuClassId(String stuClassId) {
        this.stuClassId = stuClassId;
    }
}